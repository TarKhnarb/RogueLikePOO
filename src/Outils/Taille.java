package Outils;

public class Taille{

        /*************
         * Variables *
         *************/
    private final int x;
    private final int y;

    /****************
     * Constructeur *
     ****************/
    public Taille(int x, int y){

        this.x = x;
        this.y = y;
    }

        /********
         * GetX *
         ********/
    public int getX(){

        return x;
    }

        /********
         * GetY *
         ********/
    public int getY(){

        return y;
    }
}
